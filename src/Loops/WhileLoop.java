package Loops;

public class WhileLoop {

	public static void main(String[] args)
	{
     //Whenever no limit is given for the nos. to print at that time we use do while/while loop...otherwise we can use for loop as well
		//In do while loop it gonna print atleast one no. as its gonna perform atleast one time
		int i =1;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
		
	}

}
